    private void eventoTable() {
        try{
            int row = tabela.getSelectedRow();
            String idTable = (tabela.getModel().getValueAt(row, 0).toString());
            
            String sql = "select * from BOLETIM where id='"+idTable+"' ";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                int id = rs.getInt("id");
                campoid.setText(String.valueOf(id));
                String professor = rs.getString("professor");
                campoprofessor.setText(professor);
                String materia = rs.getString("materia");
                campomateria.setText(materia);
                double nota1 = rs.getDouble("nota1");
                campoav1.setText(String.valueOf(nota1));
                double nota2 = rs.getDouble("nota2");
                campoav2.setText(String.valueOf(nota2));
                
                
                
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void deletar() {
        int opt = JOptionPane.showConfirmDialog(null, "DESEJA REALMENTE DELETAR?","DELETAR",JOptionPane.YES_NO_OPTION);
        int row = tabela.getSelectedRow();
        int id = (int) (tabela.getModel().getValueAt(row, 0));
        if(opt==0){
            String sql = "delete from BOLETIM where id=?";
            try {
                pst = conn.prepareStatement(sql);
                pst.setInt(1, id);
                pst.execute();
                refreshList();
            } catch (Exception e) {
            }
        }
        
    }
