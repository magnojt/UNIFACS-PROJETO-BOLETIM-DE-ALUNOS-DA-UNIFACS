   private void refreshList(){
       String sql = "select * from BOLETIM";
       try {
           pst = conn.prepareStatement(sql);
           rs = pst.executeQuery();
           tabela.setModel(DbUtils.resultSetToTableModel(rs));
       } catch (Exception e) {
           JOptionPane.showMessageDialog(null, e);
       }
   }

